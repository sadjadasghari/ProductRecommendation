import torch
from transformers import CLIPProcessor, CLIPModel
from PIL import Image

class GenAIEvaluator:
    """
    Automated Evaluation Suite for Retail Image Generation.
    Uses OpenAI's CLIP model to compute text-to-image similarity (CLIP Score).
    """
    def __init__(self, model_id="openai/clip-vit-base-patch32"):
        print(f"🚀 Initializing GenAI Evaluator with {model_id}...")
        self.device = "cuda" if torch.cuda.is_available() else ("mps" if torch.backends.mps.is_available() else "cpu")
        self.model = CLIPModel.from_pretrained(model_id).to(self.device)
        self.processor = CLIPProcessor.from_pretrained(model_id)
        self.model.eval()

    def calculate_clip_score(self, image: Image.Image, text_prompt: str) -> float:
        """
        Calculates the cosine similarity (CLIP Score) between a generated image
        and the text prompt that generated it.
        A higher score means the image better visually represents the prompt.
        """
        inputs = self.processor(
            text=[text_prompt], 
            images=image, 
            return_tensors="pt", 
            padding=True
        ).to(self.device)

        with torch.no_grad():
            outputs = self.model(**inputs)
            
            # The similarities are logits. We scale them down to standard Cosine distance typical for CLIP scores
            logits_per_image = outputs.logits_per_image  # image-text similarity score
            score = logits_per_image.item() / 100.0      # typical CLIP scaling
            
        return score

def run_evaluation_demo():
    print("🔹 Running Generative AI CLIP Evaluation Demo")
    
    # 1. Initialize Evaluator
    evaluator = GenAIEvaluator()
    
    # 2. Setup a Dummy Evaluation Scenario
    base_product = "A high quality canvas tote bag"
    user_modifications = [
        "Dark moody floral pattern",
        "Bright neon yellow color",
        "Made entirely of clear plastic"
    ]
    
    print("\n[MOCK EVALUATION] Testing text-image alignment:")
    # We create a dummy black image to test the pipeline executes correctly without generating real diffusion images
    # In reality, you'd load images generated by `src.generation.image_generator.py`
    dummy_image = Image.new("RGB", (512, 512), color="black")
    
    for prompt in user_modifications:
        full_prompt = f"{base_product}, {prompt}"
        try:
            score = evaluator.calculate_clip_score(dummy_image, full_prompt)
            print(f"  • Prompt: '{full_prompt}' => CLIP Score: {score:.4f}")
        except Exception as e:
            print(f"  ❌ Error evaluating '{prompt}': {e}")
            print("  Note: Running this requires the 'transformers' library to be installed.")

    print("\n✅ GenAI Evaluation suite ready. To benchmark the actual generator, pass its outputs to `calculate_clip_score()`.")

if __name__ == "__main__":
    run_evaluation_demo()
